// Select using Get Element By Id
const titleId = document.getElementById('title');
console.log(titleId);

// Select using Get Elements By ClassName
const titleClass = document.getElementsByClassName('title');
console.log(titleClass);

// Select using Get Element By TagName
const h1Tag = document.getElementsByTagName('h1');
console.log(h1Tag);

// Select using Query Selector
const paragraph = document.querySelector('.lead');
console.log(paragraph);

// Select using Query Selector All
const paragraphs = document.querySelectorAll('.lead');
console.log(paragraphs);
